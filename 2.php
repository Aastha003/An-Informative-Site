			 <p>In a world paralysed by fear of global warming, energy shortage, and resource depletion, 
			 an unexpected hero arises: High Performance Computing (HPC). An HPC system aggregates the power of tens of
			 thousands of processors interconnected by low latency optical networks to run large-scale scientific applications.
			 They support research in fields when it is practically impossible to advance only through experimentation and 
			 observation. For instance, research on weather models, ground water movements, or new energy sources rely on 
			 simulations and data analysis performed on increasingly larger HPC systems. </p>
           <p>   However, traditional HPC schedulers can no longer efficiently manage the new complex scientific 
			  applications of even more sophisticated newer systems. In his doctoral dissertation at Umeå University, 
			  Gonzalo Rodrigo has developed new techniques and tools to manage HPC systems more efficiently, increasing 
			  their capacity to support advanced scientific research.</p>
           <p> In his doctoral dissertation, Gonzalo Rodrigo at the Department of Computing Science at Umeå University,
			presents methods and tools to efficiently schedule application and workflows in High Performance Computing
			Systems and increase the speed of the scientific work they can produce.
            "In detail, I have provided a better understanding of trends of current workloads and I have developed
			a general application-oriented scheduling model in HPC systems, a scheduling simulation framework to support
			future research on scheduling algorithms, and a scheduling technique for efficient execution of complex scientific
			workflows," says Gonzalo Rodrigo. The outcome of this work also includes two open-source projects that will enable 
			future research on HPC scheduling.
           Work leading to Gonzalo Rodrigo's dissertation has been conducted in collaboration with researchers
		   from the Data Science and Technology Department and the National Energy Research Scientific Computation 
		   Center at the Lawrence Berkeley National Lab in the US.</p>


