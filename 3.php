 			 <p class="lead">Computers learn to understand humans better by modelling them </p>
			 <p>Researchers from Aalto University, University of Birmingham and University of Oslo present 
			 results paving the way for computers to learn psychologically plausible models of individuals simply
			 by observing them. In newly published conference article, the researchers showed that just by observing
			 how long a user takes to click menu items, one can infer a model that reproduces similar behavior and accurately
			 estimates some characteristics of that user's visual system, such as fixation durations.</p>
			 <div class="col-md-5 col-md-offset-0" >
