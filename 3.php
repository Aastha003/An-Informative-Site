			 <p>Despite significant breakthroughs in artificial intelligence, it has been notoriously hard for computers 
			 to understand why a user behaves the way she does. Cognitive models that describe individual capabilities,
			 as well as goals, can much better explain and hence be able to predict individual behavior also in new circumstances.
			 However, learning these models from the practically available indirect data has been out of reach.</p>
			 <p>"The benefit of our approach is that much smaller amount of data is needed than for 'black box' methods.
			 Previous methods for performing this type of tuning have either required extensive manual labor, or a large 
			 amount of very accurate observation data, which has limited the applicability of these models until now", 
			 Doctoral student Antti Kangasrääsiö from Aalto University explains.</p>
             <p>The method is based on Approximate Bayesian Computation (ABC), which is a machine learning method that
			 has been developed to infer very complex models from observations, with uses in climate sciences and
			 epidemiology among others. It paves the way for automatic inference of complex models of human behavior from
			 naturalistic observations. This could be useful in human-robot interaction, or in assessing individual capabilities
			 automatically, for example detecting symptoms of cognitive decline.</p>



